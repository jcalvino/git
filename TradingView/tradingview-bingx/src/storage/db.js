// ─────────────────────────────────────────────────────────────────
//  SQLite Database
//  Initializes tables and exports the db instance.
// ─────────────────────────────────────────────────────────────────

import { DatabaseSync } from "node:sqlite";
import { mkdirSync, existsSync } from "fs";
import { dirname } from "path";
import config from "../config/index.js";

// Ensure data directory exists
const dbDir = dirname(config.dbPath);
if (!existsSync(dbDir)) mkdirSync(dbDir, { recursive: true });

export const db = new DatabaseSync(config.dbPath);

// WAL mode: allows concurrent reads while one writer is active
db.exec("PRAGMA journal_mode = WAL");
// Wait up to 5s before giving up on a locked write — eliminates "database is locked" errors
db.exec("PRAGMA busy_timeout = 5000");
db.exec("PRAGMA foreign_keys = ON");
// Reduce fsync calls — safe with WAL, improves write throughput
db.exec("PRAGMA synchronous = NORMAL");

// ── Schema ─────────────────────────────────────────────────────
db.exec(`
  -- Signal alerts generated by the scanner
  CREATE TABLE IF NOT EXISTS signals (
    id          INTEGER PRIMARY KEY AUTOINCREMENT,
    symbol      TEXT NOT NULL,
    direction   TEXT NOT NULL CHECK (direction IN ('LONG', 'SHORT')),
    score       REAL NOT NULL,
    trade_type  TEXT NOT NULL DEFAULT 'SWING',
    price       REAL NOT NULL,
    entry       REAL NOT NULL,
    sl          REAL NOT NULL,
    tp1         REAL NOT NULL,
    tp2         REAL NOT NULL,
    tp3         REAL NOT NULL,
    position_size REAL NOT NULL DEFAULT 0,
    position_value REAL NOT NULL DEFAULT 0,
    risk_dollars  REAL NOT NULL DEFAULT 0,
    breakdown   TEXT,         -- JSON: scoring breakdown
    inputs      TEXT,         -- JSON: analysis inputs
    status      TEXT NOT NULL DEFAULT 'PENDING_APPROVAL'
                  CHECK (status IN ('PENDING_APPROVAL', 'APPROVED', 'REJECTED', 'EXPIRED', 'BELOW_THRESHOLD', 'SUPERSEDED')),
    superseded_by INTEGER REFERENCES signals(id),
    created_at  TEXT NOT NULL DEFAULT (datetime('now')),
    updated_at  TEXT NOT NULL DEFAULT (datetime('now'))
  );

  -- Executed trades (linked to signals)
  CREATE TABLE IF NOT EXISTS trades (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    signal_id     INTEGER REFERENCES signals(id),
    symbol        TEXT NOT NULL,
    direction     TEXT NOT NULL CHECK (direction IN ('LONG', 'SHORT')),
    trade_type    TEXT NOT NULL DEFAULT 'SWING',
    entry_price   REAL NOT NULL,
    size          REAL NOT NULL,
    sl_price      REAL NOT NULL,
    tp1_price     REAL NOT NULL,
    tp2_price     REAL NOT NULL,
    tp3_price     REAL NOT NULL,
    exit_price    REAL,
    pnl           REAL,
    pnl_pct       REAL,
    fees          REAL DEFAULT 0,
    status        TEXT NOT NULL DEFAULT 'OPEN'
                    CHECK (status IN ('OPEN', 'PARTIAL', 'CLOSED', 'STOPPED')),
    close_reason  TEXT,  -- 'TP1' | 'TP2' | 'TP3' | 'SL' | 'MANUAL'
    bingx_order_id TEXT,
    paper_trade   INTEGER DEFAULT 1,
    opened_at     TEXT NOT NULL DEFAULT (datetime('now')),
    closed_at     TEXT,
    updated_at    TEXT NOT NULL DEFAULT (datetime('now'))
  );

  -- Partial closes (when TPs are hit)
  CREATE TABLE IF NOT EXISTS trade_closes (
    id          INTEGER PRIMARY KEY AUTOINCREMENT,
    trade_id    INTEGER NOT NULL REFERENCES trades(id),
    close_type  TEXT NOT NULL,   -- 'TP1' | 'TP2' | 'TP3' | 'SL' | 'MANUAL'
    price       REAL NOT NULL,
    size        REAL NOT NULL,
    pnl         REAL NOT NULL,
    closed_at   TEXT NOT NULL DEFAULT (datetime('now'))
  );

  -- Open positions (synced with BingX)
  CREATE TABLE IF NOT EXISTS positions (
    id              INTEGER PRIMARY KEY AUTOINCREMENT,
    trade_id        INTEGER REFERENCES trades(id),
    symbol          TEXT NOT NULL,
    side            TEXT NOT NULL CHECK (side IN ('LONG', 'SHORT')),
    size            REAL NOT NULL,
    entry_price     REAL NOT NULL,
    mark_price      REAL,
    unrealized_pnl  REAL DEFAULT 0,
    sl_price        REAL NOT NULL DEFAULT 0,
    tp1_price       REAL NOT NULL DEFAULT 0,
    tp2_price       REAL NOT NULL DEFAULT 0,
    tp3_price       REAL NOT NULL DEFAULT 0,
    tp1_hit         INTEGER DEFAULT 0,
    tp2_hit         INTEGER DEFAULT 0,
    updated_at      TEXT NOT NULL DEFAULT (datetime('now'))
  );

  -- Daily equity snapshots
  CREATE TABLE IF NOT EXISTS snapshots (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    capital       REAL NOT NULL,
    total_pnl     REAL NOT NULL DEFAULT 0,
    day_pnl       REAL NOT NULL DEFAULT 0,
    open_positions INTEGER DEFAULT 0,
    win_count     INTEGER DEFAULT 0,
    loss_count    INTEGER DEFAULT 0,
    date          TEXT NOT NULL UNIQUE DEFAULT (date('now'))
  );

  -- Indexes
  CREATE INDEX IF NOT EXISTS idx_signals_symbol ON signals(symbol);
  CREATE INDEX IF NOT EXISTS idx_signals_status ON signals(status);
  CREATE INDEX IF NOT EXISTS idx_trades_symbol ON trades(symbol);
  CREATE INDEX IF NOT EXISTS idx_trades_status ON trades(status);
  CREATE INDEX IF NOT EXISTS idx_positions_symbol ON positions(symbol);
  CREATE INDEX IF NOT EXISTS idx_snapshots_date ON snapshots(date);
`);

// ── Migrations (additive — safe on existing DBs) ───────────────
// Each ALTER TABLE is wrapped in try/catch because SQLite throws
// if the column already exists. IF NOT EXISTS is SQLite 3.37+.
const migrations = [
  // v2: 5-setup strategy fields
  `ALTER TABLE signals ADD COLUMN setup_id      TEXT`,
  `ALTER TABLE signals ADD COLUMN setup_name    TEXT`,
  `ALTER TABLE signals ADD COLUMN rationale     TEXT`,     // JSON array of strings
  `ALTER TABLE signals ADD COLUMN leverage      INTEGER DEFAULT 1`,
  // v2: trades inherit setup context from signal
  `ALTER TABLE trades  ADD COLUMN setup_id      TEXT`,
  `ALTER TABLE trades  ADD COLUMN leverage      INTEGER DEFAULT 1`,
  // v3: scale-in entries
  `ALTER TABLE signals ADD COLUMN scale_entries TEXT`,     // JSON: [{index,price,size,value}]
  `ALTER TABLE signals ADD COLUMN avg_entry     REAL`,     // weighted avg of scale entries
  `ALTER TABLE trades  ADD COLUMN avg_entry_price REAL`,   // actual avg fill price
  `ALTER TABLE trades  ADD COLUMN scale_orders  TEXT`,     // JSON: [{index,price,orderId}]
];

for (const sql of migrations) {
  try {
    db.exec(sql);
  } catch {
    // Column already exists — safe to ignore
  }
}

export default db;
